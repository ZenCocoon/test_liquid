class PostsController < ApplicationController
  before_action :set_post, only: [:show, :edit, :update, :destroy]

  # GET /posts
  # GET /posts.json
  def index
    @posts = Post.all

    liquid_markup = '
      <div id="posts">
        {% for post in posts %}
          <div class="panel">
            <div class="panel-heading">
              <h3 class="panel-title">{{ post.title }}
              <ul class="list-unstyled list-inline pull-right">
                <li><a href="{{ post.url }}"><i class="icon-eye-open"></i> Show</a></li>
                <li><a href="{{ post.edit_url }}"><i class="icon-pencil"></i> Edit</a></li>
                <li><a href="{{ post.url }}" data-method="delete" data-confirm="Are you sure?"><i class="icon-trash"></i> Destroy</a></li>
              </ul></h3>
            </div>
            {{ post.body }}
          </div>
        {% endfor %}
      </div>'
    @template = Liquid::Template.parse(liquid_markup)
  end

  # GET /posts/1
  # GET /posts/1.json
  def show
  end

  # GET /posts/new
  def new
    @post = Post.new
  end

  # GET /posts/1/edit
  def edit
  end

  # POST /posts
  # POST /posts.json
  def create
    @post = Post.new(post_params)

    respond_to do |format|
      if @post.save
        format.html { redirect_to @post, notice: 'Post was successfully created.' }
        format.json { render action: 'show', status: :created, location: @post }
      else
        format.html { render action: 'new' }
        format.json { render json: @post.errors, status: :unprocessable_entity }
      end
    end
  end

  # PATCH/PUT /posts/1
  # PATCH/PUT /posts/1.json
  def update
    respond_to do |format|
      if @post.update(post_params)
        format.html { redirect_to @post, notice: 'Post was successfully updated.' }
        format.json { head :no_content }
      else
        format.html { render action: 'edit' }
        format.json { render json: @post.errors, status: :unprocessable_entity }
      end
    end
  end

  # DELETE /posts/1
  # DELETE /posts/1.json
  def destroy
    @post.destroy
    respond_to do |format|
      format.html { redirect_to posts_url }
      format.json { head :no_content }
    end
  end

  private
    # Use callbacks to share common setup or constraints between actions.
    def set_post
      @post = Post.find(params[:id])
    end

    # Never trust parameters from the scary internet, only allow the white list through.
    def post_params
      params.require(:post).permit(:title, :body)
    end
end
